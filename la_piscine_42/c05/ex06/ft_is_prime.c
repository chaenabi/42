int		ft_is_prime(int nb)
{
	int i;
	int	cnt;

	if (nb <= 1)
		return (0);
	i = 2;
	cnt = 0;
	while (i < nb)
	{
		if (nb % i == 0)
			cnt++;
		i++;
	}
	if (cnt == 0)
		return (1);
	return (0);
}
