#include <unistd.h>

void	ft_is_negative(int n)
{
	char np;
	char p;

	np = 'N';
	p = 'P';
	if (n < 0)
	{
		write(1, &np, 1);
	}
	else
	{
		write(1, &p, 1);
	}
}
